package com.annular.traning;

public class SwapNumbers
{

	public static void main(String[] args)
	{
//		Swap two numbers 

		  int x =39;
		  int y=28;
		 
		  System.out.println("before "+x +"  "+ y);  
	      int temp = x;  
	       x = y;  
	       y = temp;   
	       System.out.println("After "+x +"   " + y);

	}

}
